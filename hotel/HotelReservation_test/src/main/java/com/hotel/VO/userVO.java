package com.hotel.VO;

//유저 객체
public class userVO {
	private String id_user;
	private String pw_user;
	private String name_user;
	private String tel_user;
	private String email_user;
	private String type_user;
	private String age_user;
	
	
	public String getAge_user() {
		return age_user;
	}
	public void setAge_user(String age_user) {
		this.age_user = age_user;
	}
	public String getId_user() {
		return id_user;
	}
	public void setId_user(String id_user) {
		this.id_user = id_user;
	}
	public String getPw_user() {
		return pw_user;
	}
	public void setPw_user(String pw_user) {
		this.pw_user = pw_user;
	}
	public String getName_user() {
		return name_user;
	}
	public void setName_user(String name_user) {
		this.name_user = name_user;
	}
	public String getTel_user() {
		return tel_user;
	}
	public void setTel_user(String tel_user) {
		this.tel_user = tel_user;
	}
	public String getEmail_user() {
		return email_user;
	}
	public void setEmail_user(String email_user) {
		this.email_user = email_user;
	}
	public String getType_user() {
		return type_user;
	}
	public void setType_user(String type_user) {
		this.type_user = type_user;
	}
	
}
